#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	len;

	len = 0;
	while (len < dstsize)
	{
	dst[len] = src[len];
	len++;
	}
	dst[len] = 0;
	return (strlen(src));
}
